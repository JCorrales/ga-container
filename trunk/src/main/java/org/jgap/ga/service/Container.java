package org.jgap.ga.service;

/**
 *
 * @author Tabiul Mahmood
 */
public class Container {
    private int containerId;
    private double weight;
    private double flammableindex;
    private int port;

    public double getWeight() {
        return weight;
    }

    public void setWeight(double weight) {
        this.weight = weight;
    }

    public double getFlammableindex() {
        return flammableindex;
    }

    public void setFlammableindex(double flammableindex) {
        this.flammableindex = flammableindex;
    }

    public int getPort() {
        return port;
    }

    public void setPort(int port) {
        this.port = port;
    }

    public int getContainerId() {
        return containerId;
    }

    public void setContainerId(int containerId) {
        this.containerId = containerId;
    }
    
    

}
